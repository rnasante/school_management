# PowerShell script to remove node_modules from Git tracking
Write-Host "Removing node_modules from Git tracking..."
git rm -r --cached frontend/node_modules --quiet
Write-Host "Adding all other changes..."
git add .
Write-Host "Committing changes..."
git commit -m "Remove node_modules from Git tracking"
Write-Host "Pushing changes to remote..."
git push origin frontend
Write-Host "Done!" 